% Підготовка даних
% Чорні точки (Центральна група)
X_black = [ 0.0  0.3 -0.3  0.5 -0.5  0.2 -0.2  0.4 -0.4  0.0;
            0.5  0.4  0.4  0.2  0.2  0.6  0.6  0.0  0.0  0.3];
num_black = size(X_black, 2);

% Білі точки (Три групи "охоронців")
X0_BL = [-1.5 -1.2 -1.0 -1.3 -0.9; -1.0 -0.8 -1.2 -0.3 -0.5]; % Низ-ліво
X0_BR = [ 1.2  1.5  1.0  1.3  0.9; -1.0 -0.8 -1.1 -0.3 -0.5]; % Низ-право
X0_T  = [ 0.0  0.3 -0.3  0.5 -0.5  0.2;  1.5  1.8  1.7  1.6  1.4  1.3]; % Верх

X_white = [X0_BL, X0_BR, X0_T];
num_white = size(X_white, 2);

% Об'єднуємо координати
X = [X_black, X_white];

% Формування цілей (T) для 3-х нейронів
% Кожен нейрон має видати "0" тільки для "своєї" групи білих точок
T_black = ones(3, num_black); 
T_white = [zeros(1,5), ones(1,5),  ones(1,6);   % Нейрон 1 "відсікає" BL
           ones(1,5),  zeros(1,5), ones(1,6);   % Нейрон 2 "відсікає" BR
           ones(1,5),  ones(1,5),  zeros(1,6)]; % Нейрон 3 "відсікає" T
T = [T_black, T_white];

% Створення та навчання мережі
net = newp(X, T, 'hardlim', 'learnpn');
net.trainParam.epochs = 1000;
net = train(net, X, T);

% Тестування
Y = sim(net, X);
errors = sum(any(Y ~= T));
fprintf('Помилок: %d / %d\n', errors, size(T,2));

%% Візуалізація
figure('Color', 'w'); % Білий фон вікна

plot(X_black(1,:), X_black(2,:), 'ko', 'MarkerFaceColor', 'k', 'MarkerSize', 8); 
hold on;

plot(X_white(1,:), X_white(2,:), 'ko', 'MarkerFaceColor', 'w', 'MarkerSize', 8, 'LineWidth', 1.5);

% Малюємо лінії нейронів
for i = 1:3
    plotpc(net.IW{1,1}(i,:), net.b{1}(i));
    h = findobj(gca, 'Type', 'line');
    set(h(1), 'Color', 'k', 'LineWidth', 1); 
end

title('Класифікація (три нейрони)');
xlabel('x1'); ylabel('x2');
grid on;

% Масштаб
axis tight;
pad = 0.5;
axis([min(X(1,:))-pad max(X(1,:))+pad min(X(2,:))-pad max(X(2,:))+pad]);
%% Моделювання на нових даних
disp('--- Тестування мережі (Моделювання) ---');

% Створимо кілька нових тестових точок, яких не було в навчанні
% X_test(1,:) - координата x1
% X_test(2,:) - координата x2
X_test = [0.1,  -1.2,  1.4;
          0.3,  -0.5,  1.5];

% Проводимо моделювання (отримуємо виходи 3-х нейронів для кожної точки)
Y_test = sim(net, X_test);

% Візуалізуємо нові точки на існуючому графіку
figure(1); hold on;

for j = 1:size(X_test, 2)
    % Точка вважається "Чорною" (належить до центральної групи) ТІЛЬКИ ЯКЩО 
    % всі 3 нейрони видали '1' (тобто Y_test(:, j) == [1; 1; 1]).
    if all(Y_test(:, j) == 1)
        fprintf('Тестова точка [%.1f; %.1f] класифікована як: ЧОРНА\n', X_test(1,j), X_test(2,j));
        % Малюємо тестову точку зеленим квадратом, якщо вона класифікована як чорна
        plot(X_test(1,j), X_test(2,j), 'gs', 'MarkerSize', 10, 'LineWidth', 2);
    else
        fprintf('Тестова точка [%.1f; %.1f] класифікована як: БІЛА\n', X_test(1,j), X_test(2,j));
        % Малюємо тестову точку червоним хрестиком, якщо вона класифікована як біла
        plot(X_test(1,j), X_test(2,j), 'rx', 'MarkerSize', 10, 'LineWidth', 2);
    end
end

title('Класифікація та Моделювання');