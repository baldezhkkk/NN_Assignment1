%% Підготовка навчальних даних
% Вхідні вектори (кожен стовпець — окремий образ)
X = [-1.5  1.5 -0.5  2.0;
      1.5  2.0  0.3  1.0];

% Цільові значення класів для кожного вектора
T = [0  0  1  1];

%% Формування архітектури персептрона 
% Формування одношарового персептрона за допомогою функції 'newp'
% 'hardlim' — активаційна функція, 'learnp' — функція навчання.
net = newp(X, T, 'hardlim', 'learnp');

%% Ініціалізація мережі 
% Присвоєння вагам та зміщенням початкових нульових значень
net = init(net);

%% Навчання мережі 
% Встановлення ліміту епох (за замовчуванням 1000)
net.trainParam.epochs = 1000;
% Функція train автоматично відкриває вікно з процесом навчання
net = train(net, X, T);

%% Тестування (моделювання) мережі 
% Використовуємо функцію sim для отримання прогнозів мережі
Y = sim(net, X);

% Вивід результатів у командне вікно
fprintf('Очікувані класи (T): '); disp(T)
fprintf('Отримані класи (Y):  '); disp(Y)

% Розрахунок кількості помилок
errors = sum(abs(Y - T));
fprintf('Кількість помилок після навчання: %d\n', errors);

%% Побудова графіка (Візуалізація) 
figure;
% Відображення вхідних образів на площині
plotpv(X, T);
hold on;
% Побудова лінії, що розділяє класи (на основі ваг та зміщення)
plotpc(net.IW{1,1}, net.b{1});

title('Класифікація 2D образів (одношаровий персептрон)');
xlabel('Ознака X1'); ylabel('Ознака X2');
legend('Клас 0', 'Клас 1', 'Розділяюча лінія');
grid on;